//and number of years provided by the user. Enter principal, rate and time through the
//console using the Scanner class.
//Interest = Principal* Rate* Time

package com.revature.cja;

public class InterestOnPrincipal {

	// method to display the rate of interest
	public double interest(double principal, double rate, double time) {
		return principal * rate * time;
	}
}
