CREATE OR REPLACE PROCEDURE INSERTCUSTOMER(CU_ID IN VARCHAR2,CU_US IN VARCHAR2, CU_PS IN VARCHAR2,CU_NM IN VARCHAR2,CU_SC IN VARCHAR2)
AS
BEGIN
INSERT INTO BANKCUSTOMERS VALUES(CU_ID,CU_US,CU_PS,CU_NM,CU_SC);
COMMIT;
END;
/

CREATE OR REPLACE PROCEDURE BANKINSERT(ACCID IN VARCHAR2, BALZ IN VARCHAR2, CUSTID IN VARCHAR2)
AS
BEGIN
INSERT INTO BANKACCOUNTS VALUES(ACCID, BALZ, CUSTID);
COMMIT;
END;
/
CREATE OR REPLACE PROCEDURE BANKUPDATE(BALZ IN VARCHAR2, CUSTID IN VARCHAR2)
AS
BEGIN
UPDATE BANKACCOUNTS
SET  BALANCE = BALZ 
WHERE CUSTOMERID = CUSTID;
COMMIT;
END;


CREATE OR REPLACE PROCEDURE USERDELETE(CUSTID IN VARCHAR2)
AS
BEGIN
DELETE FROM BANKCUSTOMERS WHERE CUSTID = CUSTID;

COMMIT;
END;