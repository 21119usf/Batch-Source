CREATE TABLE BANKCUSTOMERS (
CUSTOMERID VARCHAR2(30) PRIMARY KEY,
USERNAME VARCHAR2(30),
PASSWORD VARCHAR2(30),
NAME VARCHAR2(30),
SOCIAL VARCHAR2(30)
);
/
CREATE TABLE BANKACCOUNTS (
ACCOUNTID VARCHAR2(30) PRIMARY KEY,
BALANCE VARCHAR2(30)
);
